        public override string[] GetRolesForUser(string username)
        {
            using (Context db = new Context())
            {
                var user = db.Users.Find(int.Parse(username.Split('|')[0]));
                if (user == null)
                {
                    return new string[0];
                }
                else
                {
                    return user.Roles.Select(r => r.Name).ToArray();
                }
            }
